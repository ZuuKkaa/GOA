# შემოვიტანოთ ორი ცვლადი და მოვახდინოთ შემდეგი მოქმედებები

num1 = input("Enter first number:")
# პირველი ცვლადი
num2 = input("Enter second number:")
# მეორე ცვლადი

# მოვახდინოთ პირველი მოქმედება : მეტობა
print(num1 > num2)
# მოვახდინოთ მეორე მოქმედება : ნაკლებობა
print(num1 < num2)
# მოვახდინოთ მესამე მოქმედება : მეტობა და ტოლობა
print(num1 >= num2)
# მოვახდინოთ მეოთხე მოქმედება : ნაკლებობა და ტოლობა
print(num1 <= num2)
# მოვახდინოთ მეხუთე მოქმედება : ტოლობა
print(num1 == num2)